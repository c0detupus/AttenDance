package model;

/**
 *
 * @author kajri.qu
 */
public class Courses {

    private String name;
    private int points;

    public Courses(String name, int points) {
        name = this.name;
        points = this.points;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPoints() {
        return points;
    }

    public void setPoints(int points) {
        this.points = points;
    }

}
